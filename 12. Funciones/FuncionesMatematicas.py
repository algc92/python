import math
import random

# Elevar al cuadrado los parametros(número y exponente)
print("Elevar al cuadrado:",math.pow(10, 2))
# Raiz cuadrada flotante
print("Raiz cuadrada flotante:",math.sqrt(64))
# Raiz cuadrada entero
print("Raiz cuadrada entero:",math.isqrt(64))
# obtener seno trigonometrico
print("Seno:",math.sin(90))
# obtener coseno trigonometrico
print("Coseno:",math.cos(90))
# obtener tangente trigonometrico
print("Tangente:",math.tan(90))
# obtener factorial
print("Factorial:",math.factorial(5))
# Número aleatorio
print("Valor aleatorio:",random.randint(1, 100))