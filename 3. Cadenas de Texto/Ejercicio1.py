cadena = "Te quiero solo como amigo"

print(cadena[0 : 2])#Primeros 2 caracteres

#Cuando necesitas invertir empieza el conteo de 1
print(cadena[-3: ])#El final de la cadena

print(cadena[ : : 2])#Omite cada 2 caracteres

print(cadena[ : : -1])#Invierte la cadena de texto

print(cadena + " / " + cadena[ : : -1])